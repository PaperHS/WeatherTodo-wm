public class ThingTodoSqlHelper extends SQLiteOpenHelper {

    private static final String TEXT_TYPE = " TEXT";
    private static final String INTEGER_TYPE = " INTEGER";
    private static final String COMMA_SEP = ",";
    private static final String SQL_CREATE_ENTRIES =
            "CREATE TABLE " + ThingTodo.TABLE_NAME + " (" +
                    ThingTodo._ID + " INTEGER PRIMARY KEY," +
                    ThingTodo.THING + TEXT_TYPE + COMMA_SEP +
                    ThingTodo.TIME + INTEGER_TYPE + COMMA_SEP +
                    ThingTodo.NOTI + INTEGER_TYPE  +
                    " )";

    public ThingTodoSqlHelper(Context context ){
        super(context, "thingtodo.db", null, 1, new DatabaseErrorHandler() {
            @Override public void onCorruption(SQLiteDatabase dbObj) {

            }
        });
    }

    @Override public void onCreate(SQLiteDatabase db) {
        db.execSQL(SQL_CREATE_ENTRIES);
    }

    @Override public void onUpgrade(SQLiteDatabase db, int oldVersion, int newVersion) {

    }
}
