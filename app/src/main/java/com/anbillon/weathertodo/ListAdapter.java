public class ListAdapter extends BaseAdapter {

    List<ThingTodo> list;
    Context context;

    public ListAdapter(List<ThingTodo> list, Context context) {
        this.list = list;
        this.context = context;
    }

    @Override public int getCount() {
        return list == null?0:list.size();
    }

    @Override public ThingTodo getItem(int position) {
        return list == null?null:list.get(position);
    }

    @Override public long getItemId(int position) {
        return position;
    }

    @Override public View getView(int position, View convertView, ViewGroup parent) {
        View view = LayoutInflater.from(context).inflate(R.layout.item_noti,parent,false);
        ThingTodo todo = list.get(position);
        ((TextView)view.findViewById(R.id.tv_content)).setText(todo.thing);
        Calendar calendar = Calendar.getInstance();
        calendar.setTimeInMillis(todo.time);
        ((TextView)view.findViewById(R.id.tv_time)).setText(calendar.get(Calendar.YEAR)+"年"+(calendar.get(Calendar.MONTH)+1)+"月"
                +calendar.get(Calendar.DAY_OF_MONTH)+"日  "+calendar.get(Calendar.HOUR_OF_DAY)+":"+calendar.get(Calendar.MINUTE)
        );

        return view;
    }


}
